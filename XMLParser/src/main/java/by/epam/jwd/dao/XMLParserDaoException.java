package by.epam.jwd.dao;

public class XMLParserDaoException extends Exception{

	private static final long serialVersionUID = -183279075800034587L;

	public XMLParserDaoException() {
		super();
	}

	public XMLParserDaoException(String message, Throwable cause, boolean enableSuppression,
			boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);
	}

	public XMLParserDaoException(String message, Throwable cause) {
		super(message, cause);
	}

	public XMLParserDaoException(String message) {
		super(message);
	}

	public XMLParserDaoException(Throwable cause) {
		super(cause);
	}

}
